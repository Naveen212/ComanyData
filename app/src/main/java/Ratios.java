public class Ratios {


    private double QuickRatio;
    private double CashRatio;
    private double Debt_To_Equity_Ratio;
    private double CurrentRatio;
    private double Long_Term_Debt_Ratio;

    public Ratios()
    {

    }




    public double getQuickRatio() {
        return QuickRatio;
    }

    public void setQuickRatio(double quickRatio) {
        QuickRatio = quickRatio;
    }

    public double getCashRatio() {
        return CashRatio;
    }

    public void setCashRatio(double cashRatio) {
        CashRatio = cashRatio;
    }

    public double getDebt_To_Equity_Ratio() {
        return Debt_To_Equity_Ratio;
    }

    public void setDebt_To_Equity_Ratio(double debt_To_Equity_Ratio) {
        Debt_To_Equity_Ratio = debt_To_Equity_Ratio;
    }

    public double getCurrentRatio() {
        return CurrentRatio;
    }

    public void setCurrentRatio(double currentRatio) {
        CurrentRatio = currentRatio;
    }

    public double getLong_Term_Debt_Ratio() {
        return Long_Term_Debt_Ratio;
    }

    public void setLong_Term_Debt_Ratio(double long_Term_Debt_Ratio) {
        Long_Term_Debt_Ratio = long_Term_Debt_Ratio;
    }


}
